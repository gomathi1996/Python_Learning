# while condition
i=1
while i<=10:
    print(f"The value of the variable is {i}") #print statement
    i = i+1

print("Done Loop!!!!!")  

# For loop
for x in range (6):
    print(x)
    x = x+1

for y in range (1,5):
    print(y)
    y = y+1    

